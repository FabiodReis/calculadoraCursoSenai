
public class principal {

	public static void main(String[] args) {
		// laço de repeção for
		// Exibir os numeros de 1ao 100
		//

		System.out.println("Exibindo dos numeros de 1 a 100");
		for (int contador = 1; contador <= 100; contador = contador + 1) {

			System.out.println(contador);
		} // for

		// Exibir os numeros entre 25 e 35
		System.out.println("Exibindo numeros de 25 a 35");
		for (int i = 25; i <= 35; i++) {
			System.out.println(i);
		} // for

		// Exibir os numeros entre 1 e 10 em ordem decrescente

		System.out.println("Exibindo numeros entre 1 e 10 em ordem decrescente");
		for(int i = 10; i >= 1; i--) {
			System.out.println(i);
		}//for
		
		//Exibir os numeros multiplos de 5 entre 1 e 50
		System.out.println("Exibindo numeros Multiplos de 5 entre 1 e 50");
		for(int i = 5; i <=50; i = i+ 5) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
