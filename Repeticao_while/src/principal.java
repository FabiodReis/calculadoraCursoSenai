
public class principal {

	public static void main(String[] args) {
		// laço de repeticação -->
		// exibir os numeros de 1 a 10 com for

		for (int i = 1; i <= 10; i++) {
			System.out.print(i + ",");
		}

		System.out.println("\n--------------------------------");
		// exibir os numeros
		int numero = 1;
		while (numero <= 10) {
			System.out.println(numero);
			numero++;
		}//while

	}

}
